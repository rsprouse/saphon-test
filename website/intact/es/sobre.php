<h5>Sobre SAPhon</h5>

Actualmente la base de datos de SAPhon incluye inventarios de <?=$n_lang?> lenguas y variedades.
</p>

La página web de SAPhon fue creada para compartir datos sobre los inventarios fonológicos de lenguas indígenas de Sur América que puedan ser de utilidad a investigaciones lingüísticas y proyectos educativos. Los inventarios fonológicos pueden ser accesados a través de un <a href=".">vistazo al mapa</a>, la <a href="listas.php">lista de lenguas</a> o la función de <a href="fonemas.php">búsqueda de fonemas</a>.</p>

Si está interesado en saber como se recopilaron los datos y como se preparó su presentación en el sitio web, accese la página de <a href="metodología.php">metodología</a>. Para aprender como citar datos obtenida del sitio web de SAPhon, por favor consulte la página  con información sobre cómo <a href="citar.php">citar</a>.
</p>

Otra de las metas del sitio web de SAPhon es atraer comentarios y correcciones de especialistas para mejorar la calidad de este recurso compartido. Si tiene comentarios y/o correcciones a la base de datos de SAPhon, por favor visite la sección para <a href="contribuya.php">contribuciones</a>. También haremos disponible una versión de la base de datos que facilitará el análisis cuantitativo una vez hayamos incorporado comentarios de especialistas que consideremos esenciales a estos propósitos. Esta página es <a href="actualizaciones.php">actualizada</a> regularmente para así reflejar adiciones y correcciones, los cambios mayores serán reflejados por la iteración del número de versión del sitio web y de la base de datos. </p>

La base de datos de SAPhon y el sitio web son mantenidos por <a href="http://linguistics.berkeley.edu/people/person_detail.php?person=183">Lev Michael</a> y <a href="http://linguistics.berkeley.edu/people/person_detail.php?person=418">Emily Clem</a> en <a href="http://linguistics.berkeley.edu">UC Berkeley</a>. La base de datos de SAPhon fue desarrollado por Tammy Stark, Will Chang, Anna Currey, Elizabeth Pierce y Andrea Guerrero, y el sitio web the SAPhon fue desarrollado por Will Chang, Hannah Haney, Tammy Stark, y Kelsey Neely. Las versiones en español y en portugués de esta página fueron traducidas por Marilola Pérez y Krista Brune respectivamente.
</p>

