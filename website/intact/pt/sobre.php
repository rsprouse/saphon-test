<h5>Sobre SAPhon</h5>

A base de dados de SAPhon atualmente inclui inventários de <?=$n_lang?> línguas e variedades.
</p>

A página web de SAPhon foi criada principalmente para compartilhar dados dos inventários fonológicos das línguas indígenas sul-americanas que possam ser de utilidade a pesquisas lingüísticas e projetos educativos. Os inventários fonológicos podem ser acessados através de uma <a href=".">olhada ao mapa</a>, da <a href="listas.php">tabela de línguas</a> que pode ser ordenada segundo distintas categorias ou de uma <a href="fonemas.php">procura dos fonemas</a>.
</p>

Se estiver interessado em saber como se recopilaram os dados e como se preparou a sua apresentação no sítio web, acesse a página de <a href="metodologia.php">metodologia</a>. Para aprender como citar os dados obtidos no sítio web de SAPhon, por favor consulte a página com informação sobre como <a href="citar.php">citar</a>.
</p>

Outro objetivo importante do sítio web de SAPhon é atrair comentários e correções de especialistas para melhorar a qualidade deste recurso compartilhado. Se tiver comentários ou correções à base de dados de SAPhon, por favor visite a secção para <a href="contribua.php">contribuições</a>. Teremos disponível uma versão da base de dados que facilitará a análise quantitativa quando incorporarmos os comentários de especialistas que consideremos essenciais a estes propósitos. Esta página é  <a href="atualizações.php">atualizada</a> regularmente para assim reflexionar as adições e correções à base de dados com as modificações principais indicadas através dos números de versões do sítio web e da base de dados.</p> 

A base de dados de SAPhon e o sitio web são mantidos por <a href="http://linguistics.berkeley.edu/people/person_detail.php?person=183">Lev Michael</a> e <a href="http://linguistics.berkeley.edu/people/person_detail.php?person=418">Emily Clem</a> em <a href="http://linguistics.berkeley.edu">UC Berkeley</a>. A base de dados de SAPhon foi desenvolvida por Tammy Stark, Will Chang, Anna Currey, Elizabeth Pierce e Andrea Guerrero, e o sítio web foi desenvolvido por Will Chang, Hannah Haney, Tammy Stark e Kelsey Neely. As versões em espanhol e em português desta página foram traduzidas por Marilola Pérez e Krista Brune respectivamente. </p>

