<h5>Agradecimentos</h5>

Nosso mais profundo agradecimento vai dirigido a todos os falantes de línguas indígenas sul-americanos os quais têm dedicado muito tempo e esforço para compartilhar suas línguas com linguistas de todas partes do mundo. O estudo destas línguas importantes seria impossível sem a dedicação, paciência e boa vontade dos seus falantes.
</p>

Muitos linguistas contribuíram com manuscritos inéditos de inventários fonológicos à base de dados de SAPhon e também nos mandaram seus trabalhos publicados. Nestes casos a informação do colaborador é indicado na página do inventário fonológico correspondente. Queremos agradecer mais uma vez sua valiosa contribuição.
</p>

Além disso, muitos colegas assistiram na recopilação de inventários fonológicas das fontes publicadas, compartilharam recursos difíceis de obter conosco, ou ajudaram resolver assuntos confusos relacionados a classificação linguística. Queremos aproveitar esta oportunidade para agradecer-lhes: Willem F. H. Adelaar, Joshua Birchall, Martine Bruil, Lyle Campbell, Mateus Carvalho, María Chavarría, Mily Crevels, Spike Gildea, Antoine Guillaume, Harald Hammarström, Helder Ferreira, Sergio Meira, Yoram Meroz, Denny Moore, Janice Nuckolls, Zachary O'Hagan, Luis Oquendo, Erin O'Rourke, Simon Overall, Eduardo Ribeiro, Luis Miguel Rojas Berscia, Françoise Rose, Jorge Emilio Rosés Labrada, Frank Seifart, Kristine Stenzel, Adam Tallman, Pilar Valenzuela, and Leo Wetzels. (Por favor não duvidem em lembrar-nos se sem querer omitimos seu nome desta lista!) Em particular, queremos agradecer Mark Donohue por compartilhar conosco o ‘princípio de codificação exaustiva de inventários’ no qual se apoia a base de dados de SAPhon e Ron Sprouse por compartilhar sua habilidade técnica conosco durante as primeiras etapas deste projeto.
