<h5> Frequently Asked Questions </h5>

<ol>
<li>
<b>Q: How do I find the phonology for a language I'm interested in?</b>
</p>
<b>A:</b> There are three ways to locate phonologies on the current version of the SAPhon site: 1) the <a href=".">map browse</a> function; 2) the <a href="lists.php">language name table</a>; and 3) the <a href="phonemes.php">phoneme search</a> function.</p>
</li>
<li>
<b>Q: The inventories have characters that my browser cannot display, or the IPA symbols do not look good.  How do I fix that?</b>
</p>
<b>A:</b> We recommend installing <a href="http://scripts.sil.org/cms/scripts/page.php?site_id=nrsi&id=Gentium_download#02b091ae">Gentium Plus</a>, a free font suitable for IPA characters.
</p>
</li>
<li>
<b>Q: The location given for a language on the map does not coincide with the location given in a published resource or with my own knowledge about the language. Why is that and what can be done about it?</b>
</p>
<b>A:</b> There are a number of possible reasons for such discrepancies, each of which has a different corresponding solution:
</p>
<ol style="list-style-type: lower-roman">
<li>
We made a mistake.
</p>
If you believe this is the case, please inform us regarding the correct location (with latitude and longitude coordinates). And whenever possible, please direct us towards published information regarding the correct location.
</p>
</li><li>
Some languages have large geographical distributions (e.g. Mapudungun), and in this version of the SAPhon site, we are representing languages as single points, rather than as continuous polygonal distributions.
</p>
If you believe that there is a compelling reason to change the point marked within the polygon, please indicate which point seems superior (with latitude and longitude coordinates) and please indicate why this is a preferable point.
</p>
</li><li>
Some languages have discontinuous distributions, and in this version of the SAPhon site, we are representing languages as single points, forcing us to select one point.
</p>
If you believe that there is a compelling reason to change the point we selected, please indicate the single point that seems superior to our current selection (with latitude and longitude coordinates) and please indicate why this is a preferable point.
</p>
</li>
</ol>
</li>
<li>
<b>Q: Why is one language name used and not another?</b>
</p>
<b>A:</b> We have made an effort to adopt language names that are most widely used by specialists familiar with the particular language or language family. 
</p>
</li>
<li>
<b>Q: You don't seem to have an inventory for a language that I have worked on, or one that I know has a published phonological inventory. How can this information be added?</b>
</p>
<b>A:</b> Please contact us at contribute.saphon@gmail.com. We are eager to add reliable unpublished phonological inventories as well as inventories from published sources that we were unable to obtain access to. Note, however, that in the latter case, we restrict ourselves to resources which include a phonological analysis of some nature. In particular, we do not ourselves infer inventories from resources that consist solely of wordlists that have not been subjected to phonological analysis, or from resources that only provide a list of phones.
</p>
</li>
<li>
<b>Q: Why don't you include information on allophony?</b>
</p>
<b>A:</b> Whereas phonological inventories are provided for most languages that have been the focus of at least some modern linguistic attention, descriptions of allophony are much less common. At present, we are interested in developing a relatively uniform and dense dataset, rending the inclusion of allophonic information problematic.
</li>
</ol>
